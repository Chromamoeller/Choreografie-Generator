export class LateinFigure {
  name;
  level;
  startingFoot;
  freeFootAfterFinish;
  startsInPP;
  endsInPP;

  constructor(
    name,
    level,
    startingFoot,
    freeFootAfterFinish,
    startsInPP,
    endsInPP
  ) {
    this.name = name;
    this.level = level;
    this.startingFoot = startingFoot;
    this.freeFootAfterFinish = freeFootAfterFinish;
    this.startsInPP = startsInPP;
    this.endsInPP = endsInPP;
  }
}
